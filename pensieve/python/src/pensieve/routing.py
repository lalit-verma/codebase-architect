# Parsing
# ---------------------------------------------------------------------------


def _extract_yaml_block(content: str) -> str | None:
    """Extract the first YAML code block from markdown content.

    Looks for ```yaml ... ``` fences. Returns the YAML text or None.
    """
    match = re.search(r"```yaml\s*\n(.*?)```", content, re.DOTALL)
    if match:
        return match.group(1)
    return None


def _parse_routing_map(routing_map_path: Path) -> tuple[list[dict], list[dict], list[str]]:
    """Parse routing-map.md and extract subsystem + pattern routing entries.

    Returns (subsystem_entries, pattern_entries, errors).
    """
    errors: list[str] = []

    if not routing_map_path.exists():
        return [], [], [f"routing-map.md not found at {routing_map_path}"]

    try:
        content = routing_map_path.read_text(encoding="utf-8")
    except OSError as e:
        return [], [], [f"Failed to read routing-map.md: {e}"]

    yaml_text = _extract_yaml_block(content)
    if yaml_text is None:
        return [], [], ["No ```yaml``` block found in routing-map.md"]

    try:
        data = yaml.safe_load(yaml_text)
    except yaml.YAMLError as e:
        return [], [], [f"YAML parse error in routing-map.md: {e}"]

    if not isinstance(data, dict):
        return [], [], [f"routing-map.md YAML root is not a dict: {type(data).__name__}"]

    subsystems = data.get("subsystem_routing", [])
    patterns = data.get("pattern_routing", [])

    if not isinstance(subsystems, list):
        errors.append(f"subsystem_routing is not a list: {type(subsystems).__name__}")
        subsystems = []

    if not isinstance(patterns, list):
        errors.append(f"pattern_routing is not a list: {type(patterns).__name__}")
        patterns = []

    return subsystems, patterns, errors


def _parse_recursion_from_analysis_state(state_path: Path) -> dict[str, list[str]]:
    """Extract recursive subsystem children from .analysis-state.md.

    Handles two formats:
    1. Inline in subsystems_completed:
       "retrieval-rag (recursive: vector, web-search, loaders)"
    2. Structured recursion_applied block (from deep-dive prompt):
       recursion_applied:
         - parent: retrieval-rag
           children:
             - name: vector

    Returns {parent_name: [child_name, ...]}. Empty dict if no recursion
    or file not found.
    """
    if not state_path.exists():
        return {}

    try:
        content = state_path.read_text(encoding="utf-8")
    except OSError:
        return {}

    # Extract YAML frontmatter between --- fences
    match = re.match(r"^---\s*\n(.*?)\n---", content, re.DOTALL)
    if not match:
        yaml_text = _extract_yaml_block(content)
        if not yaml_text:
            return {}
    else:
        yaml_text = match.group(1)

    try:
        data = yaml.safe_load(yaml_text)
    except yaml.YAMLError:
        return {}

    if not isinstance(data, dict):
        return {}

    result: dict[str, list[str]] = {}

    # Format 1: inline in subsystems_completed
    # The YAML parser may interpret "name (recursive: a, b)" as either:
    #   - a string (if quoted): "name (recursive: a, b)"
    #   - a dict (unquoted): {"name (recursive": "a, b)"}
    for entry in data.get("subsystems_completed", []):
        if isinstance(entry, str) and "(recursive:" in entry:
            # Properly quoted string case
            match_rec = re.match(r"^(.+?)\s*\(recursive:\s*(.+?)\)\s*$", entry)
            if match_rec:
                parent = match_rec.group(1).strip()
                children = [c.strip() for c in match_rec.group(2).split(",") if c.strip()]
                if parent and children:
                    result[parent] = children
        elif isinstance(entry, dict):
            # YAML misparse case: {"name (recursive": "child1, child2)"}
            for key, value in entry.items():
                if "(recursive" in key and isinstance(value, str):
                    parent = key.split("(recursive")[0].strip()
                    # Value has trailing ")" from the YAML misparse
                    children_str = value.rstrip(")").strip()
                    children = [c.strip() for c in children_str.split(",") if c.strip()]
                    if parent and children:
                        result[parent] = children

    # Format 2: structured recursion_applied block
    for entry in data.get("recursion_applied", []):
        if isinstance(entry, dict):
            parent = entry.get("parent", "")
            children = []
            for child in entry.get("children", []):
                if isinstance(child, dict):
                    children.append(child.get("name", ""))
                elif isinstance(child, str):
                    children.append(child)
            if parent and children:
                result[parent] = children

    return result


