@dataclass
class ReconciliationReport:
    """Summary of lock vs candidate reconciliation."""
    lock_created: bool = False
    subsystems_matched: int = 0
    subsystems_new: int = 0
    subsystems_stale: int = 0
    patterns_matched: int = 0
    patterns_new: int = 0
    patterns_stale: int = 0
    locked_role_overrides: int = 0
    locked_common_tasks_overrides: int = 0
    locked_brief_paths_overrides: int = 0
    locked_pattern_name_overrides: int = 0
    stale_subsystems: list[str] = field(default_factory=list)
    stale_patterns: list[str] = field(default_factory=list)
    new_subsystems: list[str] = field(default_factory=list)
    new_patterns: list[str] = field(default_factory=list)


def load_routing_lock(lock_path: Path) -> tuple[str, dict | None]:
    """Load routing-lock.yaml.

    Returns (status, data) where status is one of:
      - "missing" — file does not exist (first run)
      - "malformed" — file exists but is not valid YAML or not a dict
      - "ok" — file loaded successfully

    Callers must distinguish missing (safe to create) from malformed
    (should warn, not silently overwrite).
    """
    if not lock_path.exists():
        return ("missing", None)
    try:
        content = lock_path.read_text(encoding="utf-8")
        data = yaml.safe_load(content)
        if not isinstance(data, dict):
            return ("malformed", None)
        return ("ok", data)
    except (OSError, yaml.YAMLError):
        return ("malformed", None)


def derive_lock_from_index(index: RouteIndex) -> dict:
    """Derive a routing-lock.yaml structure from a built RouteIndex.

    Extracts only the locked semantic fields:
      - subsystems: role, common_tasks, brief_paths
      - patterns: pattern_name
    """
    lock: dict = {"version": 1, "subsystems": {}, "patterns": {}}

    for route in index.subsystem_routes:
        lock["subsystems"][route.subsystem] = {
            "role": route.role,
            "common_tasks": list(route.common_tasks),
            "brief_paths": list(route.brief_paths),
        }

    for pattern in index.pattern_routes:
        lock["patterns"][pattern.pattern_name] = {
            "pattern_name": pattern.pattern_name,
        }

    return lock


def save_routing_lock(lock: dict, lock_path: Path) -> Path:
    """Write routing-lock.yaml with deterministic key ordering."""
    lock_path.parent.mkdir(parents=True, exist_ok=True)

    # Sort subsystems and patterns for deterministic output
    ordered: dict = {"version": lock.get("version", 1)}

    subsystems = lock.get("subsystems", {})
    ordered["subsystems"] = {k: subsystems[k] for k in sorted(subsystems)}

    patterns = lock.get("patterns", {})
    ordered["patterns"] = {k: patterns[k] for k in sorted(patterns)}

    lock_path.write_text(
        yaml.dump(ordered, default_flow_style=False, sort_keys=False, allow_unicode=True),
        encoding="utf-8",
    )
    return lock_path


def merge_candidate_with_lock(
    candidate: RouteIndex,
    lock: dict,
) -> tuple[RouteIndex, ReconciliationReport]:
    """Merge a candidate RouteIndex with a routing lock.

    Lock wins for semantic fields (role, common_tasks, brief_paths, pattern_name).
    Candidate wins for structural fields (owns_paths, key_files, doc_path, etc.).

    New entries (in candidate but not lock) are included in the result.
    Stale entries (in lock but not candidate) are excluded from the result.

    Returns (merged_index, reconciliation_report).
    """
    report = ReconciliationReport()
    locked_subs = lock.get("subsystems", {})
    locked_pats = lock.get("patterns", {})

    # --- Merge subsystems ---
    candidate_sub_names = {r.subsystem for r in candidate.subsystem_routes}
    locked_sub_names = set(locked_subs.keys())

    matched_subs = candidate_sub_names & locked_sub_names
    new_subs = candidate_sub_names - locked_sub_names
    stale_subs = locked_sub_names - candidate_sub_names

    report.subsystems_matched = len(matched_subs)
    report.subsystems_new = len(new_subs)
    report.subsystems_stale = len(stale_subs)
    report.stale_subsystems = sorted(stale_subs)
    report.new_subsystems = sorted(new_subs)

    merged_subsystems: list[SubsystemRoute] = []
    for route in candidate.subsystem_routes:
        if route.subsystem in locked_subs:
            locked = locked_subs[route.subsystem]
            # Lock wins for semantic fields
            locked_role = locked.get("role")
            if locked_role is not None and locked_role != route.role:
                report.locked_role_overrides += 1
            locked_tasks = locked.get("common_tasks")
            if locked_tasks is not None and locked_tasks != route.common_tasks:
                report.locked_common_tasks_overrides += 1
            locked_brief = locked.get("brief_paths")
            if locked_brief is not None and locked_brief != route.brief_paths:
                report.locked_brief_paths_overrides += 1

            merged_subsystems.append(SubsystemRoute(
                subsystem=route.subsystem,
                # Structural fields: always from candidate
                doc_path=route.doc_path,
                owns_paths=route.owns_paths,
                key_files=route.key_files,
                key_tests=route.key_tests,
                recursive_children=route.recursive_children,
                # Semantic fields: lock if present, else candidate
                role=locked.get("role", route.role) if locked.get("role") is not None else route.role,
                common_tasks=locked.get("common_tasks", route.common_tasks) if locked.get("common_tasks") is not None else route.common_tasks,
                brief_paths=locked.get("brief_paths", route.brief_paths) if locked.get("brief_paths") is not None else route.brief_paths,
            ))
        else:
            # New subsystem — use candidate as-is
            merged_subsystems.append(route)

    merged_subsystems.sort(key=lambda r: r.subsystem)

    # --- Merge patterns ---
    candidate_pat_names = {p.pattern_name for p in candidate.pattern_routes}
    locked_pat_names = set(locked_pats.keys())

    matched_pats = candidate_pat_names & locked_pat_names
    new_pats = candidate_pat_names - locked_pat_names
    stale_pats = locked_pat_names - candidate_pat_names

    report.patterns_matched = len(matched_pats)
    report.patterns_new = len(new_pats)
    report.patterns_stale = len(stale_pats)
    report.stale_patterns = sorted(stale_pats)
    report.new_patterns = sorted(new_pats)

    merged_patterns: list[PatternRoute] = []
    for pattern in candidate.pattern_routes:
        if pattern.pattern_name in locked_pats:
            locked = locked_pats[pattern.pattern_name]
            locked_name = locked.get("pattern_name")
            if locked_name and locked_name != pattern.pattern_name:
                report.locked_pattern_name_overrides += 1
            # Pattern name from lock; structural fields from candidate
            merged_patterns.append(PatternRoute(
                pattern_name=locked_name if locked_name else pattern.pattern_name,
                doc_anchor=pattern.doc_anchor,
                subsystem=pattern.subsystem,
                template_file=pattern.template_file,
                registration=pattern.registration,
                test_template=pattern.test_template,
                file_count=pattern.file_count,
            ))
        else:
            # New pattern — use candidate as-is
            merged_patterns.append(pattern)

    merged_patterns.sort(key=lambda r: r.pattern_name)

    merged_index = RouteIndex(
        version=candidate.version,
        generated_from=candidate.generated_from,
        subsystem_routes=merged_subsystems,
        pattern_routes=merged_patterns,
        fallbacks=candidate.fallbacks,
        fallback_hint=candidate.fallback_hint,
        errors=candidate.errors,
    )

    return merged_index, report


def update_lock_with_new_entries(
    lock: dict,
    index: RouteIndex,
    report: ReconciliationReport,
) -> dict:
    """Auto-append new entries from the merged index into the lock.

    New subsystems and patterns are added. Stale entries are retained
    in the lock but excluded from route-index (handled by merge).
    """
    updated = {
        "version": lock.get("version", 1),
        "subsystems": dict(lock.get("subsystems", {})),
        "patterns": dict(lock.get("patterns", {})),
    }

    # Add new subsystems
    for name in report.new_subsystems:
        route = next((r for r in index.subsystem_routes if r.subsystem == name), None)
        if route:
            updated["subsystems"][name] = {
                "role": route.role,
                "common_tasks": list(route.common_tasks),
                "brief_paths": list(route.brief_paths),
            }

    # Add new patterns
    for name in report.new_patterns:
        pattern = next((p for p in index.pattern_routes if p.pattern_name == name), None)
        if pattern:
            updated["patterns"][name] = {
                "pattern_name": pattern.pattern_name,
            }

    return updated
