"""Route-index v2 builder with routing lock support.

Compiles a machine-readable route-index.json from:
  - agent-docs/routing-map.md (YAML-in-markdown, generated by /analyze-synthesize)
  - agent-docs/routing-lock.yaml (stable routing semantics, auto-generated on first run)
  - agent-docs/.analysis-state.md (optional, for recursive subsystem metadata)

Three-layer model:
  - routing-map.md = latest synthesized candidate (may drift across re-runs)
  - routing-lock.yaml = stable reviewed routing semantics (locked fields)
  - route-index.json = compiled runtime artifact from both

Locked fields (from routing-lock.yaml when present):
  - subsystem: role, common_tasks, brief_paths

Fresh fields (always from routing-map.md):
  - subsystem: doc_path, owns_paths, key_files, key_tests, recursive_children
  - pattern: all fields (matched by exact pattern_name)

Pattern locking limitation:
  Patterns are matched by exact name. If synthesis renames or drops a
  pattern, it becomes stale + new — not a preserved rename. The lock
  stabilizes subsystem semantics reliably but only tracks pattern
  existence, not identity across renames.

Design:
  - Deterministic: same inputs → same output
  - Defensive: malformed lock → warn and skip, not silently overwrite
  - Inspectable: JSON/YAML with readable field names
  - Stable: locked subsystem semantics survive re-synthesis drift
"""

from __future__ import annotations

import json
import re
from dataclasses import asdict, dataclass, field
from pathlib import Path

import yaml


# ---------------------------------------------------------------------------
# Schema
# ---------------------------------------------------------------------------


@dataclass
class SubsystemRoute:
    """One subsystem's routing entry."""
    subsystem: str
    doc_path: str
    role: str = ""
    owns_paths: list[str] = field(default_factory=list)
    key_files: list[str] = field(default_factory=list)
    key_tests: list[str] = field(default_factory=list)
    common_tasks: list[str] = field(default_factory=list)
    brief_paths: list[str] = field(default_factory=list)
    recursive_children: list[str] = field(default_factory=list)


@dataclass
class PatternRoute:
    """One pattern's routing entry."""
    pattern_name: str
    doc_anchor: str = ""
    subsystem: str = ""
    template_file: str = ""
    registration: str = ""
    test_template: str = ""
    file_count: str = ""


@dataclass
class RouteIndex:
    """Complete route-index v2."""
    version: int = 2
    generated_from: dict = field(default_factory=dict)
    subsystem_routes: list[SubsystemRoute] = field(default_factory=list)
    pattern_routes: list[PatternRoute] = field(default_factory=list)
    fallbacks: dict = field(default_factory=dict)
    fallback_hint: str = ""
    errors: list[str] = field(default_factory=list)

    def to_dict(self) -> dict:
        return {
            "version": self.version,
            "generated_from": self.generated_from,
            "subsystem_routes": [asdict(r) for r in self.subsystem_routes],
            "pattern_routes": [asdict(r) for r in self.pattern_routes],
            "fallbacks": self.fallbacks,
            "fallback_hint": self.fallback_hint,
            "errors": self.errors,
        }

    def to_json(self, indent: int = 2) -> str:
        return json.dumps(self.to_dict(), indent=indent)


