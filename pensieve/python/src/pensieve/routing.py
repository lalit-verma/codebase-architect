def _slugify(name: str) -> str:
    """Convert a name to a URL-friendly slug for doc anchors."""
    slug = name.lower().strip()
    slug = re.sub(r"[^a-z0-9]+", "-", slug)
    slug = slug.strip("-")
    return slug


def build_route_index(
    routing_map_path: Path,
    analysis_state_path: Path | None = None,
) -> RouteIndex:
    """Build a route-index v2 from routing-map.md and optionally .analysis-state.md.

    Args:
        routing_map_path: Path to agent-docs/routing-map.md.
        analysis_state_path: Path to agent-docs/.analysis-state.md (optional).

    Returns:
        RouteIndex with all routing entries, or errors if parsing failed.
    """
    subsystem_entries, pattern_entries, errors = _parse_routing_map(routing_map_path)

    # Parse recursion metadata if available
    recursion: dict[str, list[str]] = {}
    if analysis_state_path:
        recursion = _parse_recursion_from_analysis_state(analysis_state_path)

    # Build subsystem routes
    subsystem_routes: list[SubsystemRoute] = []
    for entry in subsystem_entries:
        if not isinstance(entry, dict):
            errors.append(f"Skipping non-dict subsystem entry: {entry}")
            continue

        name = str(entry.get("name", ""))
        if not name:
            errors.append(f"Skipping subsystem entry with no name: {entry}")
            continue

        # Derive doc path from name
        safe_name = _slugify(name)
        doc_path = entry.get("doc", f"agent-docs/subsystems/{safe_name}.md")

        owns = [str(p) for p in entry.get("owns_paths", [])]
        # brief_paths: use explicit value from routing-map if provided,
        # otherwise fall back to owns_paths for backward compatibility.
        explicit_brief = entry.get("brief_paths")
        if isinstance(explicit_brief, list) and explicit_brief:
            brief = [str(p) for p in explicit_brief]
        else:
            brief = list(owns)

        subsystem_routes.append(SubsystemRoute(
            subsystem=name,
            doc_path=doc_path,
            role=str(entry.get("role", "")),
            owns_paths=owns,
            key_files=[str(f) for f in entry.get("key_files", [])],
            key_tests=[str(t) for t in entry.get("key_tests", [])],
            common_tasks=[str(t) for t in entry.get("common_tasks", [])],
            brief_paths=brief,
            recursive_children=recursion.get(name, []),
        ))

    # Sort subsystem routes by name for deterministic output
    subsystem_routes.sort(key=lambda r: r.subsystem)

    # Build pattern routes
    pattern_routes: list[PatternRoute] = []
    for entry in pattern_entries:
        if not isinstance(entry, dict):
            errors.append(f"Skipping non-dict pattern entry: {entry}")
            continue

        pattern_name = str(entry.get("pattern", ""))
        if not pattern_name:
            errors.append(f"Skipping pattern entry with no name: {entry}")
            continue

        slug = _slugify(pattern_name)
        doc_anchor = f"patterns.md#{slug}"

        pattern_routes.append(PatternRoute(
            pattern_name=pattern_name,
            doc_anchor=doc_anchor,
            subsystem=str(entry.get("subsystem", "")),
            template_file=str(entry.get("template_file", "")),
            registration=str(entry.get("registration", "")),
            test_template=str(entry.get("test_template", "")),
            file_count=str(entry.get("file_count", "")),
        ))

    # Sort pattern routes by name for deterministic output
    pattern_routes.sort(key=lambda r: r.pattern_name)

    # Build sources list
    generated_from: dict = {
        "routing_map": str(routing_map_path),
    }
    if analysis_state_path and analysis_state_path.exists():
        generated_from["analysis_state"] = str(analysis_state_path)

    return RouteIndex(
        version=2,
        generated_from=generated_from,
        subsystem_routes=subsystem_routes,
        pattern_routes=pattern_routes,
        fallbacks={
            "nano": "agent-docs/agent-context-nano.md",
            "agent_context": "agent-docs/agent-context.md",
            "structural_profiles": "agent-docs/structural-profiles.md",
        },
        fallback_hint=(
            "Codebase context in CLAUDE.md (nano-digest). "
            "For deeper context: agent-docs/agent-context.md"
        ),
        errors=errors,
    )


def save_route_index(index: RouteIndex, output_path: Path) -> Path:
    """Write route-index.json."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(index.to_json() + "\n", encoding="utf-8")
    return output_path


